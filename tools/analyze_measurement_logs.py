#!/usr/bin/env python3
"""Analyze LaserBench measurement logs.

The script parses the dedicated measurement logs generated by LaserBench and
reports whether the executed scan respected the configured spacing, timing and
pause parameters. It works on a single file or on a whole directory.

Examples
--------
python tools/analyze_measurement_logs.py
python tools/analyze_measurement_logs.py LaserBenchC++/build/Release/logs/measurements
python tools/analyze_measurement_logs.py file1.log file2.log
"""

