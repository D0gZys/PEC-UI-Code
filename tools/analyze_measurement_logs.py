EVENT_RE = re.compile(r"^\[(?P<ts>\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2}\.\d{3})\] \[(?P<cat>[A-Z_]+)\] (?P<body>.*)$")
POINT_MM_RE = re.compile(r"\(([-+0-9.eE]+),([-+0-9.eE]+)\)\s*mm")
POINT_PX_RE = re.compile(r"\(([-+0-9]+),([-+0-9]+)\)\s*px")
MOTOR_RE = re.compile(r"X=([-+0-9.eE]+)\s+Y=([-+0-9.eE]+)(?:\s*mm)?")
FRACTION_RE = re.compile(r"^\s*(\d+)\s*/\s*(\d+)\s*$")


@dataclass
class Event:
    timestamp: datetime
    category: str
    body: str
    tags: list[str] = field(default_factory=list)
    fields: dict[str, Any] = field(default_factory=dict)


@dataclass
class ScanConfig:
    technique: str = ""
    mode: str = ""
    zone_shape: str = ""
    channel: int | None = None
    expected_points: int | None = None
    lines: int | None = None
    columns: int | None = None
    selection_start_mm: tuple[float, float] | None = None
    selection_end_mm: tuple[float, float] | None = None
    selection_width_mm: float | None = None
    selection_height_mm: float | None = None
    selection_diag_mm: float | None = None
    selection_start_px: tuple[int, int] | None = None
    selection_end_px: tuple[int, int] | None = None
    selection_width_px: int | None = None
    selection_height_px: int | None = None
    step_mm: float | None = None
    dwell_s: float | None = None
    reposition_speed_mm_s: float | None = None
    sample_pitch_mm: float | None = None
    scan_speed_mm_s: float | None = None
    trigger_kind: str | None = None
    trigger_value: float | None = None
    row_step_mm: float | None = None
    start_corner: str | None = None
    primary_axis: str | None = None
    simple_period_s: float | None = None


@dataclass
class MeasurementLog:
    path: Path
    header_lines: list[str]
    config: ScanConfig
    events: list[Event]


@dataclass
class Acquisition:
    order: int
    kind: str
    timestamp: datetime
    t_abs: datetime | None
    t_pot_s: float | None
    row: int | None
    col: int | None
    target_mm: tuple[float, float] | None
    motor_mm: tuple[float, float] | None
    ewe_v: float | None
    current_a: float | None


@dataclass
class AnalysisResult:
    log: MeasurementLog
    acquisitions: list[Acquisition]
    tracking_rows: list[dict[str, Any]]
